CREATE PROCEDURE SP_INSERTA_DETALLE_COMPRA
	@COD_COMPRA INT,
	@COD_PRODUCTO INT,
	@CANTIDAD INT,
	@PRECIO_COMPRA VARCHAR(8),
	@PRECIO_VENTA VARCHAR(8),
	@PRESENTACION VARCHAR(50),
	@DESCRIPCION VARCHAR(50),
	@COD_MARCA INT
AS
BEGIN

	UPDATE COMPRA
	SET
	ESTADO = 'A'
	WHERE COD_COMPRA = @COD_COMPRA

	DECLARE @PRODUCTOEXISTE INT
	SELECT @PRODUCTOEXISTE = COD_PRODUCTO FROM PRODUCTO WHERE COD_PRODUCTO = @COD_PRODUCTO
	IF (@PRODUCTOEXISTE != '')
		BEGIN

			UPDATE PRODUCTO
			SET
			PRECIO_VENTA=@PRECIO_VENTA,
			STOCK = STOCK + @CANTIDAD
			WHERE COD_PRODUCTO = @COD_PRODUCTO

			INSERT INTO DETALLE_COMPRA (COD_COMPRA, COD_PRODUCTO, CANTIDAD, PRECIO_COMPRA, PRECIO_VENTA, SUBTOTAL, IGV, TOTAL) 
			VALUES (@COD_COMPRA, @COD_PRODUCTO, @CANTIDAD, @PRECIO_COMPRA, @PRECIO_VENTA, (@CANTIDAD*@PRECIO_VENTA),
			(0.18*(@CANTIDAD*@PRECIO_VENTA)), ((@CANTIDAD*@PRECIO_VENTA)+(0.18*(@CANTIDAD*@PRECIO_VENTA))))
		END
	ELSE
		BEGIN
			SELECT @COD_PRODUCTO=(MAX(COD_PRODUCTO)+1) FROM PRODUCTO
				
			INSERT INTO PRODUCTO(COD_PRODUCTO, COD_MARCA, DESCRIPCION, PRECIO_VENTA, PRESENTACION, STOCK, FECHA_REGISTRO)
			VALUES (@COD_PRODUCTO, @COD_MARCA, @DESCRIPCION, @PRECIO_VENTA, @PRESENTACION, @CANTIDAD, GETDATE())

			INSERT INTO DETALLE_COMPRA (COD_COMPRA, COD_PRODUCTO, CANTIDAD, PRECIO_COMPRA, PRECIO_VENTA, SUBTOTAL, IGV, TOTAL) 
			VALUES (@COD_COMPRA, @COD_PRODUCTO, @CANTIDAD, @PRECIO_COMPRA, @PRECIO_VENTA, (@CANTIDAD*@PRECIO_VENTA),
			(0.18*(@CANTIDAD*@PRECIO_VENTA)), ((@CANTIDAD*@PRECIO_VENTA)+(0.18*(@CANTIDAD*@PRECIO_VENTA))))
		END
END
GO

