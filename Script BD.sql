--PROCEDIMIENTO LOGIN
CREATE PROCEDURE SP_LOGIN
    @USUARIO VARCHAR(50),
    @CLAVE VARCHAR(50)
AS
BEGIN
    SELECT CONCAT(T.NOMBRES,' ', T.APE_PATERNO, ' ', T.APE_MATERNO) AS NOMBRES, U.USUARIO, U.CLAVE, C.DESCRIPCION, T.COD_TRABAJADOR
    FROM TRABAJADORES T INNER JOIN CARGO C ON T.COD_CARGO = C.COD_CARGO INNER JOIN USUARIOS U ON T.COD_TRABAJADOR = U.COD_TRABAJADOR
	WHERE USUARIO=@USUARIO AND CLAVE=@CLAVE AND T.ESTADO = 'A'
END
GO

